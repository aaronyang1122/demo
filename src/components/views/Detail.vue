                <p>Magic Leap涉嫌视频造假的新闻引发媒体广泛报道。这家估值高达45亿美元，但5年来从未推出产品的独角兽公司陷入空前信任危机。就在Magic Leap迟迟难以完成产品化之际，一家名为智视科技的上海公司低调推出全球首款消费级增强现实（AR）眼镜。《大麦村报》获得确切消息，这款AR眼镜将于12月16日亮相北京国际创新创业博览会，并于2017年1月5日登陆CES（美国国际消费类电子产品展览会）。</p>
                <p>没有对比就没有伤害，同样参展CES的Magic Leap将无法躲避来自中国同行的直接挑战。仅有不足一个月的时间，Magic Leap神话或将再遭重创？</p>
                <p>大麦村小编带着当下最火爆的问题探营位于张江高科技园区的上海智视科技有限公司（以下简称智视科技），创始人吴峰诚恳解读风口上的AR产业。</p>
                <h6>伟大需要耐心</h6>
                <p>大麦村：Magic Leap涉嫌视频造假，使其在科技爱好者心目中的信用已经濒临破产，甚至被直指骗子公司。作为AR专业人士，您怎么看？</p>
                <p>吴峰：多数朋友并不是做AR研究的，对视频产生误会是正常的。其实，Magic Leap直接用自己技术拍摄的视频是有明确标识的。而引起争议的视频，并没有做同样的标识。我想这恰恰说明了广大科技爱好者对AR眼镜的强烈期待，因此失望情绪比较大。</p>
                <p>（Magic Leap被指造假的视频，YouTube播放量近350万）</p>
                <p>大麦村：Magic Leap是骗子公司吗？</p>
                <p>吴峰：可能性不大。Magic Leap甚至可以说是一家胸怀伟大的公司。从他们高管的履历和研究背景，大家可以看出来，他们在挑战未来一代的AR技术，这是非常有勇气和价值的。通俗点讲大家就容易理解了，光波导技术是美国军方正在使用的技术，而光场显示技术是美国军方正在研发的技术。颠覆性创新从来不是一蹴而就的。采用光波导技术的HoloLens，微软也筹备和研发了7年才推出。伟大需要耐心，既需要技术研发者十年磨一剑的研发耐心，也需要我们等待、包容的耐心。希望科技爱好者能给这样的创新企业更多鼓励，也希望广大媒体朋友客观报道，多做科普。</p>
                <h6>产品就是要解决实际需求</h6>
                <p>大麦村：我们采访的多位业内人士推测Magic Leap宣称的PEQ产品将“面对现实”，采用光波导技术，但产品化仍需加以时日。我们了解到智视科技的EST眼镜也是采用光波导技术的。一家累计融资14亿美金的公司还没能推出的产品，您怎样用千万级投入就率先实现了呢？ </p>
                <p>吴峰：中美文化不同，创业环境也不同，所以我们选择的研发策略也不同。我们先实现一个小目标（笑），就是做出一款真正的“眼镜”。体积和重量要足够小，佩戴轻便；而视场角要足够大，体验震撼。所以我们将全部力量聚焦于光学模组和驱动芯片等硬件核心技术的研发，取得了一些列实质性，甚至突破性进展。用毛主席的话说，我们是集中优势兵力，打歼灭战。</p>
                <p>大麦村：国内外AR企业多数选择行业市场应用，智视科技是我们了解到的目前国内唯一一家拥有自主光波导核心技术的AR企业，为什么会选择消费类市场？</p>
                <p>吴峰：无论行业市场还是消费市场，产品的本质就是要解决实际需求，就是要有用、有价值。从哪一个市场起步，只是登山的路径不同，各有各的难度，各有各的挑战。要做消费类市场，首先就要解决轻便问题，你得符合用户佩戴习惯，AR眼镜首先得是一副眼镜。行业特殊场景，用户可能接受头盔；但在日常工作、生活场景，人们就没法接受了。我们是做硬件出身的，凭借我们自主研发的技术，我们可以不断优化核心部件。例如光学镜片，从第一代的棱镜大小缩小到现在只有小指甲大小，体积和重量大幅下降。所以我们非常有信心做出消费级产品.</p> 
                <h6>EST眼镜总重量仅125克，只有微软HoloLens重量的四分之一，接近普通太阳镜大小。视场角更大，相当于在用户3米远处形成超过100吋的屏幕，是HoloLens的2.5倍，意味着随“眼”携带家庭影院。</h6>
                <p>大麦村：投资商可以着眼未来，要赢得消费者，还是要满足实际需求。即使国际巨头谷歌和微软的AR眼镜都没有成功进入消费市场，您为什么有信心做开路先锋？</p>
                <p>吴峰：你说的非常好，就是要满足消费者实际需求！消费者需求其实一直在，就看你的产品能不能更好地满足。看3D电影是实际需求吧？现在你只能去影院。有了EST眼镜，你在飞机上、高铁上、公园的长椅上，随时随地可以。拍摄视频是实际需求吧，现在我们多数用手机录像，但坐过山车、骑自行车、漂流等很多场合就很不方便。用眼镜是最自然的方式。更重要的是，今天，EST眼镜就可以做到。</p>
                <h6>AR产业合作远远大于竞争</h6>
                <p>大麦村：很多AR从业者担心Magic Leap风波加剧AR创业者融资难度，您怎么看？</p>
                <p>吴峰：我觉得只要专注做出自己的产品价值，就不用多虑。我们产品还没发布，就有很多投资机构找上门来。很多专业投资机构不仅表示要投资硬件本身，也很关注AR相关的软件企业、内容企业。为什么这么多投资机构、国际巨头看好AR？增强现实是与我们现实世界交互的，有非常多的应用场景和非常大的想象空间。解决好任何一点的实际需求都有巨大发展空间。</p>
                <p>我们智视科技专注做好自己的硬件同时，也非常乐于开放SDK和我们的平台，让更多的AR相关企业，甚至广大开发者，做出更好的产品体验，更大地发挥自身价值。</p>
                <p>大麦村：EST眼镜的巨大空间毋庸置疑，行业市场需求也很旺盛，但竞争会更加激烈，智视科技准备如何抢占行业市场？</p>
                <p>吴峰：无论消费市场还是行业市场，AR产业都是刚刚起步，发展空间巨大。我们常说，想象力有多大，AR市场的空间就有多大。因此，AR产业各企业间的合作要远远大于竞争。需要大家共同努力，为用户提供更丰富的产品和应用，共同做大市场。</p>
                <p>所以，智视科技会专注在自己的核心技术上，做好硬件本体。无论是行业应用，还是更多消费类应用，都会采取开放合作的方式，支持各领域专业企业去发展。大家发挥各自优势，才能形成更好的产业生态，真正激活AR市场，为用户创造价值。</p>
                <p>大麦村：我们广大消费者什么时候能够体验到EST眼镜？</p>
